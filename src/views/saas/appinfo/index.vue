<template>
  <div class="execution">
    <basic-container>
      <el-row :gutter="10">
        <el-col :span="this.clickObj ? 16 : 24">
          <el-card class="box-card">
            <avue-crud ref="crud"
                       v-model="form"
                       :page="page"
                       :data="tableData"
                       :permission="permissionList"
                       :table-loading="tableLoading"
                       :option="tableOption"
                       @on-load="getPage"
                       @row-update="handleUpdate"
                       @row-save="handleSave"
                       @search-change="searchChange"
                       @sort-change="sortChange"
                       @row-del="handleDel">
					   
				<template slot-scope="scope" slot="rstate">
					<el-switch
							v-model="scope.row.rstate"
							active-value="1"
							inactive-value="0"
							active-color="#13ce66"
							inactive-color="#ff4949"
							@change="changeEnable(scope.row)">
					</el-switch>
				</template>
					   
				<template slot-scope="scope" slot="menu">
					<el-button type="text"
							   icon="el-icon-s-claim"
							   size="mini"
							   plain
							   @click="handleItem(scope.row,scope.index)">应用模板
					</el-button>
				</template>
			  
            </avue-crud>
          </el-card>
        </el-col>
		
        <el-col :span="8" v-if="this.clickObj">
          <el-card class="box-card">
            <div slot="header" class="clearfix">
              <h3><i class="el-icon-document-copy"></i> 应用模板，所属应用：[{{this.clickObj.code}}]{{this.clickObj.name}} </h3>
              <el-button style="float: right; padding: 3px 0" type="text" @click="handleItem()">关闭</el-button>
            </div>
            <avue-crud ref="crud2"
                       v-model="form2"
                       :page="page2"
                       :data="tableData2"
                       :permission="permissionList"
                       :before-open="beforeOpen2"
                       :option="tableOption2"
                       :table-loading="tableLoading2"
                       @on-load="getPage2"
                       @row-update="handleUpdate2"
                       @row-save="handleSave2"
                       @row-del="handleDel2"
                       @refresh-change="refreshChange2"
                       @sort-change="sortChange2">

              <template slot-scope="scope" slot="menuLeft">
                <el-button type="primary"
                  icon="el-icon-edit"
                  size="small"
                  plain
                  @click.stop="batchSaveTpl()">保存</el-button>
              </template>

            </avue-crud>

          </el-card>
        </el-col>
      </el-row>

    </basic-container>
  </div>
</template>

<script>
  import {getPage, addObj, putObj, delObj} from '@/api/saas/appinfo'
  import {getPage2, addObj2, putObj2, delObj2, batchSaveTpl} from '@/api/saas/apptemplate'
  import {tableOption} from './appinfo'
  import {tableOption2} from './apptemplate'
  import {mapGetters} from 'vuex'

  export default {
    name: 'appinfo',
    data() {
      return {
        clickObj: null,
        form: {},
        dialogFormVisible: false,
        tableData: [],
        tableOption: tableOption,
        page: {
          total: 0, // 总页数
          currentPage: 1, // 当前页数
          pageSize: 10 // 每页显示多少条
        },
        tableLoading: false,
		
        form2: {},
        tableData2: [
          
        ],
        page2: {
          total: 0, // 总页数
          currentPage: 1, // 当前页数
          pageSize: 10 // 每页显示多少条
        },
        tableLoading2: false,
        tableOption2: tableOption2
      }
    },
	
    created() {
    },
	
    mounted: function () {
    },
	
    computed: {
      ...mapGetters(['permissions']),
      permissionList() {
        return {
          addBtn: this.vaildData(this.permissions.saas_appinfo_add, false),
          delBtn: this.vaildData(this.permissions.saas_appinfo_del, false),
          editBtn: this.vaildData(this.permissions.saas_appinfo_edit, false),
          viewBtn: this.vaildData(this.permissions.saas_appinfo_get, false)
       };
      }
    },
	
    methods: {
      sortChange(val){
        let prop = val.prop ? val.prop.replace(/([A-Z])/g,"_$1").toLowerCase() : '';
        if(val.order=='ascending'){
          this.page.descs = []
          this.page.ascs = prop
        }else if(val.order=='descending'){
          this.page.ascs = []
          this.page.descs = prop
        }else{
          this.page.ascs = []
          this.page.descs = []
        }
        this.getPage(this.page)
      },
	  
      getPage(page, params) {
        this.tableLoading = true
        getPage(Object.assign({
          current: page.currentPage,
          size: page.pageSize,
          descs: this.page.descs,
          ascs: this.page.ascs
        }, params)).then(response => {
          this.tableData = response.data.data.records
          this.page.total = response.data.data.total
          this.page.currentPage = page.currentPage
          this.page.pageSize = page.pageSize
          this.tableLoading = false
        })
      },
	  
	changeEnable(row){
		putObj({
			id: row.id,
			rstate: row.rstate
		}).then(data => {

		})
	},

    handleDel: function (row, index) {
        var _this = this
        this.$confirm('是否确认删除?', '警告', {
          confirmButtonText: '确定',
          cancelButtonText: '取消',
          type: 'warning'
        }).then(function () {
          return delObj(row.id)
        }).then(() => {
          this.tableData = []
          this.getPage(this.page)
          _this.$message({
            showClose: true,
            message: '删除成功',
            type: 'success'
          })
        }).catch(function () {
        })
      },
	  
      /**
       * @title 数据更新
       * @param row 为当前的数据
       * @param index 为当前更新数据的行数
       * @param done 为表单关闭函数
       *
       **/
      handleUpdate: function (row, index, done, loading) {
        putObj(this.filterForm(row)).then(() => {
          this.$message({
            showClose: true,
            message: '修改成功',
            type: 'success'
          })
          this.getPage(this.page)
          done()
        }).catch(() => {
          loading()
        })
      },
	  
      /**
       * @title 数据添加
       * @param row 为当前的数据
       * @param done 为表单关闭函数
       *
       **/
      handleSave: function (row, done, loading) {
        addObj(this.filterForm(row)).then(() => {
          this.$message({
            showClose: true,
            message: '添加成功',
            type: 'success'
          })
          this.getPage(this.page)
          done()
        }).catch(() => {
          loading()
        })
      },
	  
      searchChange(form,done) {
        this.getPage(this.page, this.filterForm(form))
        done()
      },

      handleItem: function (row) {
        if(row){
          this.clickObj = row
          this.page2.currentPage = 1
          this.getPage2(this.page2)
        }else{
          this.clickObj = null
        }
      },
	  
      beforeOpen2(done) {
        this.$set(this.form2,"appId",this.clickObj.id)
        done()
      },
	  
      sortChange2(val){
        let prop = val.prop ? val.prop.replace(/([A-Z])/g,"_$1").toLowerCase() : ''
        if(val.order=='ascending'){
          this.page2.descs = []
          this.page2.ascs = prop
        }else if(val.order=='descending'){
          this.page2.ascs = []
          this.page2.descs = prop
        }else{
          this.page2.ascs = []
          this.page2.descs = []
        }
        this.getPage2(this.page2)
      },
	  
      getPage2(page, params) {
        this.tableLoading2 = true
        getPage2(Object.assign({
          current: 1,
          size: 2,
          descs: 4,
          ascs: 5,
        }, params, this.paramsSearch2,{
          appId: this.clickObj.id
        })).then(response => {
			let data = response.data.data
			this.tableData2 = data
			if(data && data.length > 0){
				for(let i=0, len=data.length;i<len;i++){
					data[i].$cellEdit = true
				}
			}

          this.tableLoading2 = false
        }).catch(() => {
          this.tableLoading2=false
        })
      },
	  
	changeEnable2(row){
		putObj2({
			id: row.id,
			rstate: row.rstate
		}).then(data => {

		})
	},

      /**
       * @title 数据删除
       * @param row 为当前的数据
       * @param index 为当前删除数据的行数
       *
       **/
      handleDel2: function(row, index) {
        var _this = this
        this.$confirm('是否确认删除此数据', '提示', {
          confirmButtonText: '确定',
          cancelButtonText: '取消',
          type: 'warning'
        }).then(function() {
          return delObj2(row.id)
        }).then(data => {
          _this.$message({
            showClose: true,
            message: '删除成功',
            type: 'success'
          })
          this.getPage2(this.page2)
        }).catch(function(err) { })
      },
	  
      /**
       * @title 数据更新
       * @param row 为当前的数据
       * @param index 为当前更新数据的行数
       * @param done 为表单关闭函数
       *
       **/
      handleUpdate2: function(row, index, done, loading) {
        putObj2(row).then(data => {
          this.$message({
            showClose: true,
            message: '修改成功',
            type: 'success'
          })
          done()
          this.getPage2(this.page2)
        }).catch(() => {
          loading()
        })
      },
      /**
       * @title 数据添加
       * @param row 为当前的数据
       * @param done 为表单关闭函数
       *
       **/
      handleSave2: function(row, done, loading) {
        addObj2(row).then(data => {
          this.$message({
            showClose: true,
            message: '添加成功',
            type: 'success'
          })
          done()
          this.getPage2(this.page2)
        }).catch(() => {
          loading()
        })
      },

    batchSaveTpl: function(form, done) {
      var tplList = [];
      this.tableData2.forEach(ele => {
        if(ele.tplMode == null ||  ele.tplMode == ''){
          // 不需要提交保存
        }else{
          tplList.push(ele);
        };
      });
      batchSaveTpl(tplList).then(data => {
            this.$message({
              showClose: true,
              message: '应用模板保存成功！',
              type: 'success'
            })
            done()
            this.getPage2(this.page2)
          }).catch(() => {
            loading()
          })
      },
      
      /**
       * 刷新回调
       */
      refreshChange2(val) {
        this.getPage2(val.page)
      }
    }
  }
</script>

